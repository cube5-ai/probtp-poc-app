"""
Evaluate comparison reports generated by different pipelines.

This script evaluates the quality and accuracy of insurance comparison reports
by asking questions from the ground truth dataset and comparing LLM answers against expected answers.
"""

import asyncio
import json
import os
import sys
from pathlib import Path
from typing import Any, Literal

from dotenv import load_dotenv
from google import genai
from google.genai.types import GenerateContentConfig, ThinkingConfig
from langfuse import Langfuse
from openinference.instrumentation.google_genai import GoogleGenAIInstrumentor
from pydantic import BaseModel, Field

load_dotenv()

# Add parent directory to path for imports
sys.path.insert(0, str(Path(__file__).parent.parent.parent))

langfuse = Langfuse(
    secret_key=os.getenv("LANGFUSE_SECRET_KEY", ""),
    public_key=os.getenv("LANGFUSE_PUBLIC_KEY", ""),
    host=os.getenv("LANGFUSE_HOST", "https://cloud.langfuse.com"),
)


class EvaluationResult(BaseModel):
    """Structured evaluation result returned by the LLM."""

    explanation: str
    verdict: Literal["CORRECT", "PARTIAL", "HALLUCINATION", "MISSED"]
    missing_information: list[str] = []
    evidence_quotes: list[str] = []
    confidence: float = Field(default=0.0, ge=0.0, le=1.0)


def parse_evaluation_result(raw_json: str) -> EvaluationResult:
    """Parse and validate a JSON string into an EvaluationResult model."""
    return EvaluationResult.model_validate_json(raw_json)


class AsyncGeminiClientManager:
    """Async context manager for proper Gemini client lifecycle management."""

    def __init__(self):
        self.client = None

    async def __aenter__(self):
        GoogleGenAIInstrumentor().instrument()

        self.client = genai.Client(
            vertexai=True,
            project="probtp-poc-prod",
            location="global",
        )
        return self.client

    async def __aexit__(self, exc_type, exc_val, exc_tb):
        if self.client:
            if hasattr(self.client, "close"):
                try:
                    if asyncio.iscoroutinefunction(self.client.close):
                        await self.client.close()
                    else:
                        self.client.close()
                except Exception as e:
                    print(f"Error closing async Gemini client: {e}")
            self.client = None


def read_eval_data(file: Path) -> list[dict]:
    """Read the evaluation data from the JSON file."""
    with open(file, encoding="utf-8") as f:
        eval_data = json.load(f)
    return eval_data


async def evaluate_single_qa(
    client,
    qa: dict,
    report_markdown: str,
    pipeline_name: str,
    prompt_answer: str,
    prompt_evaluation: str,
    report_file: str = "",
) -> dict[str, Any]:
    """Evaluate a single Q&A pair asynchronously."""
    # Build context-aware question
    question_with_context = qa["question"]
    inference_note = ""

    # For Facts and Inference, add provider context
    if qa.get("section") in ["Facts", "Inference"]:
        provider = qa.get("provider", "")
        contract_level = qa.get("contract_level", "")
        if provider:
            question_with_context = f"[Question concernant {provider} {contract_level}] {qa['question']}"

        # For Inference questions, add instruction to allow logical deduction
        if qa.get("section") == "Inference":
            inference_note = "\n\nNote: This is an inference question. You may need to logically deduce the answer from related information in the report. Use your best judgment based on the available data and explicitly stated rules or conditions. It is okay to make confident inferences."

    # Extract answer from report
    answer_prompt = prompt_answer + inference_note
    answer_prompt = answer_prompt.format(report=report_markdown, question=question_with_context)
    answer_resp = await client.aio.models.generate_content(
        model="gemini-2.5-flash",
        contents=answer_prompt,
        config=GenerateContentConfig(
            thinking_config=ThinkingConfig(thinking_budget=0),
            max_output_tokens=500,
            temperature=0.0,
        ),
    )
    candidate_answer = getattr(answer_resp, "text", "").strip()

    # Evaluate answer
    eval_prompt = (
        f"{prompt_evaluation}\n\n\n"
        f"<question>\n{qa['question']}\n</question>\n\n"
        f"<ground_truth>\n{qa['answer']}\n</ground_truth>\n\n"
        f"<candidate_answer>\n{candidate_answer}\n</candidate_answer>\n"
    )
    eval_resp = await client.aio.models.generate_content(
        model="gemini-2.5-flash",
        contents=eval_prompt,
        config=GenerateContentConfig(
            response_mime_type="application/json",
            thinking_config=ThinkingConfig(thinking_budget=300),
            response_schema=EvaluationResult.model_json_schema(),
            max_output_tokens=2000,
            temperature=0.0,
        ),
    )
    raw_json = getattr(eval_resp, "text", "").strip()

    try:
        result = parse_evaluation_result(raw_json)
        result_as_dict = result.model_dump()
        result_as_dict["llm_answer"] = candidate_answer
        result_as_dict["ground_truth"] = qa["answer"]
        result_as_dict["question"] = qa["question"]
        result_as_dict["task_type"] = qa.get("task_type", "Unknown")
        result_as_dict["section"] = qa.get("section", "Unknown")
        result_as_dict["scope"] = qa.get("scope", "Unknown")
        result_as_dict["pipeline_name"] = pipeline_name
        result_as_dict["report_file"] = report_file
        result_as_dict["question_id"] = qa.get("id", "unknown")
    except Exception:
        start = raw_json.find("{")
        end = raw_json.rfind("}")
        if start != -1 and end != -1 and end > start:
            result = EvaluationResult.model_validate_json(raw_json[start : end + 1])
            result_as_dict = result.model_dump()
            result_as_dict["llm_answer"] = candidate_answer
            result_as_dict["ground_truth"] = qa["answer"]
            result_as_dict["question"] = qa["question"]
            result_as_dict["task_type"] = qa.get("task_type", "Unknown")
            result_as_dict["section"] = qa.get("section", "Unknown")
            result_as_dict["scope"] = qa.get("scope", "Unknown")
            result_as_dict["pipeline_name"] = pipeline_name
            result_as_dict["report_file"] = report_file
            result_as_dict["question_id"] = qa.get("id", "unknown")
        else:
            result = EvaluationResult(
                verdict="HALLUCINATION",
                explanation="LLM did not return valid JSON per schema.",
                missing_information=[],
                evidence_quotes=[],
                confidence=0.0,
            )
            result_as_dict = result.model_dump()
            result_as_dict["verdict"] = "FAILED_LLM_CALL"
            result_as_dict["llm_answer"] = candidate_answer
            result_as_dict["ground_truth"] = qa["answer"]
            result_as_dict["question"] = qa["question"]
            result_as_dict["task_type"] = qa.get("task_type", "Unknown")
            result_as_dict["section"] = qa.get("section", "Unknown")
            result_as_dict["scope"] = qa.get("scope", "Unknown")
            result_as_dict["pipeline_name"] = pipeline_name
            result_as_dict["report_file"] = report_file
            result_as_dict["question_id"] = qa.get("id", "unknown")

    return result_as_dict


async def evaluate_report_async(
    qa_list: list[dict], report_markdown: str, pipeline_name: str, report_file: str = ""
) -> list[dict[str, Any]]:
    """
    Async version of evaluate_report using the Google Gen AI async client with parallel execution.
    """

    prompt_answer = """
You are an AI analyst expert at extracting information from insurance comparison reports.
Read the comparison report and answer the question strictly based on it.

<report>
{report}
</report>

<question>
{question}
</question>

Reminder:
    You are an AI analyst expert at extracting information from insurance comparison reports.
    Read the report and answer the question strictly based on it.

Provide a concise (on average 1 to 30 words, 100 words max), direct answer with no preamble:
"""

    prompt_evaluation = """
You are an expert evaluator of information retrieval quality from comparison reports.

You will receive three inputs delimited by XML tags:
<question>...</question>
<ground_truth>...</ground_truth>
<candidate_answer>...</candidate_answer>

Decide a verdict using ONLY these strict definitions:
- CORRECT: Candidate fully matches ground_truth with no material omissions or additions.
- PARTIAL: Candidate is directionally right but misses required details present in ground_truth.
- HALLUCINATION: Candidate introduces content not supported by ground_truth or contradicts it.
- MISSED: Candidate states information is not available while ground_truth contains it.

Return ONLY a JSON object on a single line that strictly conforms to this schema:
{
  "verdict": "CORRECT|PARTIAL|HALLUCINATION|MISSED",
  "explanation": "string",
  "missing_information": ["string"],
  "evidence_quotes": ["string"],
  "confidence": 0.0
}

Constraints:
- Output JSON only. No prose, no markdown, no code fences.
- Keep evidence_quotes minimal and verbatim from the provided inputs, or [] if none.
- Use confidence 1.0 for obvious cases; otherwise estimate between 0.0 and 1.0.
"""

    async with AsyncGeminiClientManager() as client:
        # Process tasks in batches to avoid rate limiting
        batch_size = 5
        all_results = []

        for i in range(0, len(qa_list), batch_size):
            batch = qa_list[i : i + batch_size]
            tasks = [
                evaluate_single_qa(
                    client, qa, report_markdown, pipeline_name, prompt_answer, prompt_evaluation, report_file
                )
                for qa in batch
            ]

            # Execute batch concurrently
            batch_results = await asyncio.gather(*tasks)
            print(
                f"Batch {i // batch_size + 1} of {(len(qa_list) + batch_size - 1) // batch_size} done",
                end='\r',
            )
            all_results.extend(batch_results)

            # Add 1s pause between batches (except after the last batch)
            if i + batch_size < len(qa_list):
                await asyncio.sleep(1)

        print()  # New line after progress

    return all_results


def write_results_to_json(enriched_results: list[dict[str, Any]], pipeline_name: str, output_dir: Path):
    """Write the results to a json file in output_dir/pipeline_name.json."""
    output_dir.mkdir(parents=True, exist_ok=True)
    output_file = output_dir / f"{pipeline_name}.json"

    with open(output_file, "w", encoding="utf-8") as f:
        json.dump(enriched_results, f, indent=2, ensure_ascii=False)

    print(f"    Results saved: {output_file}")


def quick_performance_results(results: list[dict[str, Any]]) -> dict[str, float]:
    """Quick summarize the results."""
    if not results:
        return {}

    return {
        "correct": sum(1 for r in results if r["verdict"] == "CORRECT") / len(results),
        "partial": sum(1 for r in results if r["verdict"] == "PARTIAL") / len(results),
        "hallucination": sum(1 for r in results if r["verdict"] == "HALLUCINATION") / len(results),
        "missed": sum(1 for r in results if r["verdict"] == "MISSED") / len(results),
    }


async def main():
    """
    Main async function to process all pipeline reports and evaluate them.
    """
    # Setup paths
    base_dir = Path(__file__).parent.parent
    output_dir = base_dir / "output"
    evals_dir = Path(__file__).parent / "output" / "evals"
    data_dir = Path(__file__).parent / "data"

    # Get the eval data
    eval_data_file = data_dir / "dataset.json"
    if not eval_data_file.exists():
        print(f"Error: Evaluation data not found at {eval_data_file}")
        print("Please create the dataset and run data/convert_to_json.py first")
        return

    qa_list = read_eval_data(eval_data_file)
    print(f"Loaded {len(qa_list)} evaluation questions")

    # Count by section
    by_section = {}
    for qa in qa_list:
        section = qa.get("section", "Unknown")
        by_section[section] = by_section.get(section, 0) + 1

    print(f"  Facts: {by_section.get('Facts', 0)}")
    print(f"  Inference: {by_section.get('Inference', 0)}")
    print(f"  Comparisons: {by_section.get('Comparisons', 0)}")

    # Iterate over pipeline output directories
    pipeline_dirs = [
        output_dir / "taxonomy_first",
        # output_dir / "two_phase",
        # output_dir / "baseline",
        # output_dir / "project_merge",
    ]

    for pipeline_dir in pipeline_dirs:
        if not pipeline_dir.exists() or not pipeline_dir.is_dir():
            print(f"\nSkipping {pipeline_dir.name} - directory not found")
            continue

        pipeline_name = pipeline_dir.name

        # Find comparison report markdown files
        report_files = list(pipeline_dir.glob("comparison_report_*.md"))
        if not report_files:
            print(f"\nSkipping {pipeline_name} - no comparison report markdown files found")
            continue

        # Check if evaluation JSON already exists
        eval_json_path = evals_dir / f"{pipeline_name}.json"
        if eval_json_path.exists():
            print(f"\nSkipping {pipeline_name} - evaluation already exists at {eval_json_path}")
            continue

        all_results = []
        for report_file in report_files:
            print(f"\nEvaluating {pipeline_name}: {report_file.name}")

            # Read report markdown
            report_markdown = report_file.read_text(encoding="utf-8")

            # Evaluate
            results = await evaluate_report_async(qa_list, report_markdown, pipeline_name, report_file.name)

            print(f"Results for {pipeline_name}, {report_file.name}: {len(results)} evaluations")
            perf = quick_performance_results(results)
            print(f"  Correct: {perf.get('correct', 0):.1%}")
            print(f"  Partial: {perf.get('partial', 0):.1%}")
            print(f"  Hallucination: {perf.get('hallucination', 0):.1%}")
            print(f"  Missed: {perf.get('missed', 0):.1%}")

            all_results.extend(results)

        # Save results
        write_results_to_json(all_results, pipeline_name, evals_dir)
        print(f"\n Overall results for {pipeline_name}")
        perf = quick_performance_results(all_results)
        print(f"  Correct: {perf.get('correct', 0):.1%}")
        print(f"  Partial: {perf.get('partial', 0):.1%}")
        print(f"  Hallucination: {perf.get('hallucination', 0):.1%}")
        print(f"  Missed: {perf.get('missed', 0):.1%}")


if __name__ == "__main__":
    asyncio.run(main())
